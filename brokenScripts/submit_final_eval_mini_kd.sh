#!/bin/bash
# Submit mini-final KD harness eval with JSON embedded in the batch script.
set -euo pipefail

REPO=/arf/scratch/skantar/MinitronSSM
cd "${REPO}"

MINI_SRC="${REPO}/outputs/eval/10_mini_kd.json"
TEMPLATE="${REPO}/run_final_eval_mini_kd.slurm"

if ! python -c "import json; d=json.load(open('${MINI_SRC}')); assert d.get('checkpoint')"; then
  echo "missing or invalid mini-KD artifact: ${MINI_SRC}" >&2
  exit 1
fi

JOB_SCRIPT=$(mktemp /tmp/final_eval_mini.XXXXXX.slurm)
trap 'rm -f "${JOB_SCRIPT}"' EXIT

python - "${TEMPLATE}" "${JOB_SCRIPT}" "${MINI_SRC}" <<'PY'
import sys
from pathlib import Path

template_path, job_path, mini_src = sys.argv[1:4]
template = Path(template_path).read_text(encoding="utf-8")
start = "# __JSON_PAYLOAD_START__"
end = "# __JSON_PAYLOAD_END__"
if start not in template or end not in template:
    raise SystemExit(f"payload markers missing from {template_path}")

mini_json = Path(mini_src).read_text(encoding="utf-8").rstrip("\n")

payload = f"""{start}
# Generated by submit_final_eval_mini_kd.sh; do not edit by hand.
STAGE_DIR="${{TMPDIR:-/tmp}}/minitron_final_eval_mini_${{SLURM_JOB_ID}}"
mkdir -p "${{STAGE_DIR}}"
MINI_KD_JSON="${{STAGE_DIR}}/10_mini_kd.json"
cat > "${{MINI_KD_JSON}}" <<'EOF_MINI_KD_JSON'
{mini_json}
EOF_MINI_KD_JSON
{end}"""

before, rest = template.split(start, 1)
_, after = rest.split(end, 1)
Path(job_path).write_text(before + payload + after, encoding="utf-8")
PY

chmod +x "${JOB_SCRIPT}"
echo "Submitting mini-final KD harness eval with embedded JSON payload"
sbatch "${JOB_SCRIPT}"
