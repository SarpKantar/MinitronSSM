#!/bin/bash
# Submit round-2 final eval with JSON artifacts embedded in the batch script.
#
# Compute nodes can lose /arf/scratch Lustre client endpoints ("transport endpoint
# shutdown"), so small metadata JSON must be baked into the Slurm script on the
# login node instead of copied from scratch at runtime.
set -euo pipefail

REPO=/arf/scratch/skantar/MinitronSSM
cd "${REPO}"

KD_SRC="${REPO}/outputs/eval/08_kd_results_round2.json"
TOP_SRC="${REPO}/outputs/eval/06_top3.json"
TEMPLATE="${REPO}/run_final_eval_round2.slurm"

for src in "${KD_SRC}" "${TOP_SRC}"; do
  if ! python -c "import json; json.load(open('${src}'))"; then
    echo "missing or invalid JSON artifact: ${src}" >&2
    exit 1
  fi
done

JOB_SCRIPT=$(mktemp /tmp/final_eval_r2.XXXXXX.slurm)
trap 'rm -f "${JOB_SCRIPT}"' EXIT

python - "${TEMPLATE}" "${JOB_SCRIPT}" "${KD_SRC}" "${TOP_SRC}" <<'PY'
import sys
from pathlib import Path

template_path, job_path, kd_src, top_src = sys.argv[1:5]
template = Path(template_path).read_text(encoding="utf-8")
start = "# __JSON_PAYLOAD_START__"
end = "# __JSON_PAYLOAD_END__"
if start not in template or end not in template:
    raise SystemExit(f"payload markers missing from {template_path}")

kd_json = Path(kd_src).read_text(encoding="utf-8").rstrip("\n")
top_json = Path(top_src).read_text(encoding="utf-8").rstrip("\n")

payload = f"""{start}
# Generated by submit_final_eval_round2.sh; do not edit by hand.
STAGE_DIR="${{TMPDIR:-/tmp}}/minitron_final_eval_${{SLURM_JOB_ID}}"
mkdir -p "${{STAGE_DIR}}"
KD_RESULTS="${{STAGE_DIR}}/08_kd_results_round2.json"
TOP_RESULTS="${{STAGE_DIR}}/06_top3.json"
cat > "${{KD_RESULTS}}" <<'EOF_KD_JSON'
{kd_json}
EOF_KD_JSON
cat > "${{TOP_RESULTS}}" <<'EOF_TOP_JSON'
{top_json}
EOF_TOP_JSON
{end}"""

before, rest = template.split(start, 1)
_, after = rest.split(end, 1)
Path(job_path).write_text(before + payload + after, encoding="utf-8")
PY

echo "Submitting round-2 final eval with embedded JSON payload"
sbatch "${JOB_SCRIPT}"
